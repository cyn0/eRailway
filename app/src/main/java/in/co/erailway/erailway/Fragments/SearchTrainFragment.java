	private void handleTrainSelected(String uri) {
		AppUtils.hideSoftKeyboard(getActivity());

		boolean isValid = false;
		//TO-DO please find a better way
		for(String train : trains) {
			if(uri.equals(train)) {
				isValid = true;
				break;
			}
		}

		if(!isValid) {
			Toast.makeText(mContext, "Enter a valid Train number or name", Toast.LENGTH_LONG).show();
			return;
		}
		if(mIsLiveStatus) {
			TrainStartDate selectedDate = TrainStartDate.TODAY; //(TrainStartDate)mDateSpinner.getSelectedItem();
			DateFormat dateFormat= new SimpleDateFormat("yyyy-MM-dd");

			Calendar c = Calendar.getInstance();

			System.out.println(dateFormat.format(c.getTime()));
			int id = mSegmentedGroup.getCheckedRadioButtonId();
			if(id == R.id.yesterday) {
				c.add(Calendar.DATE, -1);
			} else if(id == R.id.today) {

			} else if(id == R.id.tomorrow) {
				c.add(Calendar.DATE, 1);
			}

			uri = Constants.URI_LIVE_STATUS + "/" + uri + "?date=" + dateFormat.format(c.getTime());
		} else {
			uri = Constants.URI_TRAIN_ROUTE + "/" + uri;
		}

		mWebviewFragment = WebviewFragment.newInstance(uri);
		mFragmentManager
				.beginTransaction()
				.replace(R.id.container1, mWebviewFragment)
				.commit();

		if(mIsLiveStatus) {
			showHideBothFloatingIcons(true);
		}
	}

	private void showHideBothFloatingIcons(final boolean show) {
		((MainActivity)getActivity()).showHideFoatingButton(show, R.drawable.ic_refresh_white, new OnClickListener() {
			@Override
			public void onClick(View view) {
				if(mWebviewFragment != null) {
					mWebviewFragment.reload();

				}

			}
		});

		((MainActivity)getActivity()).showHideFoatingButtonTop(show, R.drawable.ic_refresh_white, new OnClickListener() {
			@Override
			public void onClick(View view) {
				if(mWebviewFragment != null) {
					mWebviewFragment.scrollToCurrent();
				}
			}
		});
	}
